package BaseKnowLedge;
/**
 * 
 * @author codejie
 * 变量
 * 	变量的本质是一个“可操作的存储空间”
 * 	每个变量都有类型
 * 	变量的类型可以是基本数据类型，也可以是引用数据类型
 * 变量的类型
 * 	局部变量
 * 	成员变量
 * 	静态变量
 * 常量：常量通常指固定的值
 * 	常量的申明通常用final修饰
 *  常量的命名通常用全部大写
 */
public class Variable_Constant {
	public static void main(String[] args) {
		//申明一个变量
		byte a;
		short b;
		int c;
		long d;
		char e;
		String str;
		boolean f;
		//申明多个变量
		int g,h,i,j,k;
		//常量的申明
		final String A = "你好";
		final double PI = 3.14;
	}
}
